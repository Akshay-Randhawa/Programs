class IndexOf
{
public static void main (String args[])
{
String s = " India Is a Great Country";
int y = s.indexOf("I");
int z = s.indexOf("I",2);
System.out.println(y);
System.out.println(z);
int x = s.indexOf("Great");
int q = s.indexOf("Great",10);
System.out.println(x);
System.out.println(q);

}
}